  (asset) => ({
    tickerIdx: uniqueIndex("assets_ticker_idx").on(asset.ticker),
    typeIdx: index("assets_type_idx").on(asset.type),
    isActiveIdx: index("assets_is_active_idx").on(asset.isActive),
  })
