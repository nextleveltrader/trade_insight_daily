/**
 * users — Core identity table, extended for Trade Insight Daily's 3-tier gating.
 *
 * Auth.js REQUIRES id to be `text`. This is the source of truth for all FK type
 * decisions. Any table referencing a user MUST use text("user_id"), never integer.
 *
 * Tier derivation in getUserTier() reads:
 *   1. isPro = true                         → Premium
 *   2. trialEndsAt IS NOT NULL AND > now()  → Premium (trial)
 *   3. Otherwise                            → Free (ad-supported)
 *   4. No session                           → Guest
 */
export const users = pgTable("users", {
  // ── Required by @auth/drizzle-adapter ──
  id: text("id")
    .notNull()
    .primaryKey()
    .$defaultFn(() => crypto.randomUUID()),
  name: text("name"),
  email: text("email").notNull().unique(),
  emailVerified: timestamp("email_verified", { mode: "date" }),
  image: text("image"),

  // ── 3-Tier gating fields ──
  role: userRoleEnum("role").notNull().default("user"),

  /** Set to true by Stripe/PayPal/Crypto/bKash webhook on active subscription */
  isPro: boolean("is_pro").notNull().default(false),

  /**
   * Set to NOW() + 14 days at registration (both email and Google OAuth).
   * Auto-activated — no opt-in required. Non-reinstatable once expired.
   * getUserTier() treats trialEndsAt > now() as equivalent to isPro = true.
   */
  trialEndsAt: timestamp("trial_ends_at", { mode: "date" }),

  // ── Credentials auth (email/password flow) ──
  /** bcrypt hash, cost factor 12. NULL for OAuth-only users. */
  passwordHash: text("password_hash"),

  // ── Payment integration cache ──
  /** Cached to avoid repeated Stripe API lookups on checkout / portal creation */
  stripeCustomerId: text("stripe_customer_id").unique(),

  // ── Audit ──
  createdAt: timestamp("created_at", { mode: "date" }).notNull().defaultNow(),
  updatedAt: timestamp("updated_at", { mode: "date" }).notNull().defaultNow(),
});

