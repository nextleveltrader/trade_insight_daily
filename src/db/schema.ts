/**
 * subscriptions — One active row per user per gateway subscription.
 *
 * Multi-gateway design rationale:
 * ┌──────────┬─────────────────────────────────────────────────────────────────┐
 * │ Gateway  │ providerSubscriptionId format                                   │
 * ├──────────┼─────────────────────────────────────────────────────────────────┤
 * │ Stripe   │ "sub_1NxABC..."  (Stripe Subscription ID)                       │
 * │ PayPal   │ "I-BW452GLLEP1G" (PayPal Subscription ID)                       │
 * │ Crypto   │ On-chain tx hash or payment processor's order ID               │
 * │ bKash    │ bKash Transaction ID (e.g. "8CK1ABC2D3")                        │
 * └──────────┴─────────────────────────────────────────────────────────────────┘
 *
 * planType vocabulary:
 *   "monthly"  — Stripe monthly recurring / equivalent in other gateways
 *   "annual"   — Stripe annual recurring / equivalent in other gateways
 *   "one_time" — Non-recurring (Crypto, bKash one-off payments)
 *
 * Webhook handlers update `status` and `currentPeriodEnd` to drive isPro logic:
 *   - Stripe:  checkout.session.completed → active; customer.subscription.deleted → canceled
 *   - PayPal:  BILLING.SUBSCRIPTION.ACTIVATED → active; BILLING.SUBSCRIPTION.CANCELLED → canceled
 *   - Crypto:  on-chain confirmation callback → active; expiry check by cron
 *   - bKash:   payment_success callback → active; manual management
 *
 * FK type: userId = text (matches users.id — text). Bug #11 prevention.
 */
export const subscriptions = pgTable(
  "subscriptions",
  {
    id: serial("id").primaryKey(),

    // TEXT — must match users.id (text). Bug #11 prevention.
    userId: text("user_id")
      .notNull()
      .references(() => users.id, { onDelete: "cascade" }),

    provider: paymentProviderEnum("provider").notNull(),

    /** Gateway's own identifier for this subscription or order */
    providerSubscriptionId: text("provider_subscription_id").notNull(),

    status: subscriptionStatusEnum("status").notNull().default("active"),

    /** Plan duration: 'monthly' | 'annual' | 'one_time' */
    planType: text("plan_type").notNull().default("monthly"),

    currentPeriodStart: timestamp("current_period_start", { mode: "date" }),

    /**
     * getUserTier() checks: isPro = true AND currentPeriodEnd > now()
     * Updated on every billing cycle webhook event.
     */
    currentPeriodEnd: timestamp("current_period_end", { mode: "date" }),

    /** Stripe: set when user clicks "Cancel at period end" in Customer Portal */
    cancelAtPeriodEnd: boolean("cancel_at_period_end").notNull().default(false),

    canceledAt: timestamp("canceled_at", { mode: "date" }),

    /** Gateway-level trial end (separate from our 14-day platform trial) */
    trialEnd: timestamp("trial_end", { mode: "date" }),

    // ── Audit ──
    createdAt: timestamp("created_at", { mode: "date" }).notNull().defaultNow(),
    updatedAt: timestamp("updated_at", { mode: "date" }).notNull().defaultNow(),
  },
  (sub) => ({
    userIdIdx: index("subscriptions_user_id_idx").on(sub.userId),
    statusIdx: index("subscriptions_status_idx").on(sub.status),
    /**
     * Unique constraint on (provider, providerSubscriptionId) ensures idempotent
     * webhook processing — duplicate events from any gateway are safely ignored.
     */
    uniqueProviderSubIdx: uniqueIndex("subscriptions_provider_sub_id_idx").on(
      sub.provider,
      sub.providerSubscriptionId
    ),
  })
);

/**
 * payments — Immutable transaction log across all gateways.
 *
 * Every payment event writes a new row here regardless of gateway:
 *   Stripe   → invoice.payment_succeeded fires on every billing cycle renewal
 *   PayPal   → PAYMENT.CAPTURE.COMPLETED fires on order capture
 *   Crypto   → on-chain confirmation callback fires once per tx
 *   bKash    → payment_success callback fires once per transaction
 *
 * `transactionId` must be globally unique — used as an idempotency key in
 * all webhook handlers to prevent double-processing on gateway retries.
 *
 * `amount` is stored as a decimal string in the major currency unit (e.g. "9.99"
 * for $9.99 USD). Do NOT store in smallest unit (cents) here — use the provider's
 * native representation after normalisation in the webhook handler.
 *
 * `metadata` holds the raw JSON webhook payload as a string for full audit trail
 * and future dispute resolution.
 *
 * FK types:
 *   userId         = text    (matches users.id — text)
 *   subscriptionId = integer (matches subscriptions.id — serial)
 */
export const payments = pgTable(
  "payments",
  {
    id: serial("id").primaryKey(),

    // TEXT — must match users.id (text). Bug #11 prevention.
    userId: text("user_id")
      .notNull()
      .references(() => users.id, { onDelete: "cascade" }),

    // INTEGER — must match subscriptions.id (serial). Bug #11 prevention.
    subscriptionId: integer("subscription_id").references(
      () => subscriptions.id,
      { onDelete: "set null" } // keep payment record even if subscription is deleted
    ),

    provider: paymentProviderEnum("provider").notNull(),

    /**
     * Gateway-specific transaction identifier used for idempotency:
     *   Stripe:  charge_id or invoice_id  (e.g. "ch_3NxABC...")
     *   PayPal:  capture_id               (e.g. "5O190127TN364715T")
     *   Crypto:  on-chain tx hash         (e.g. "0x1abc...")
     *   bKash:   trxID                    (e.g. "8CK1ABC2D3")
     */
    transactionId: text("transaction_id").notNull(),

    /** Major currency unit (e.g. 9.99 for $9.99). Precision: 10,2 */
    amount: decimal("amount", { precision: 10, scale: 2 }).notNull(),

    /** ISO 4217 currency code (e.g. "USD", "BDT", "EUR") */
    currency: text("currency").notNull().default("USD"),

    status: paymentStatusEnum("status").notNull().default("pending"),

    /** Raw webhook payload JSON — stored for audit trail and dispute resolution */
    metadata: text("metadata"),

    // ── Audit ──
    createdAt: timestamp("created_at", { mode: "date" }).notNull().defaultNow(),
  },
  (payment) => ({
    userIdIdx: index("payments_user_id_idx").on(payment.userId),
    subscriptionIdIdx: index("payments_subscription_id_idx").on(
      payment.subscriptionId
    ),
    /**
     * Unique constraint on transactionId is the idempotency guard.
     * Webhook handlers should upsert on this key to safely handle retries.
     */
    transactionIdIdx: uniqueIndex("payments_transaction_id_idx").on(
      payment.transactionId
    ),
    providerIdx: index("payments_provider_idx").on(payment.provider),
    statusIdx: index("payments_status_idx").on(payment.status),
  })
);

