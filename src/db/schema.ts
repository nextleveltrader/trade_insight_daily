// §5 — DRIZZLE RELATIONS
// Defines the relational graph used by Drizzle's `with` query builder.
// These do not create DB constraints — foreign keys above do that.
// ═══════════════════════════════════════════════════════════════════════════════

export const usersRelations = relations(users, ({ many }) => ({
  /** All OAuth provider accounts linked to this user */
  accounts: many(accounts),
  /** Active database sessions */
  sessions: many(sessions),
  /** Posts authored by this user (admin creates these) */
  posts: many(posts),
  /** All subscription records across all gateways */
  subscriptions: many(subscriptions),
  /** Complete payment transaction history across all gateways */
  payments: many(payments),
  /** Bookmarked posts */
  savedPosts: many(savedPosts),
}));

export const accountsRelations = relations(accounts, ({ one }) => ({
  user: one(users, {
    fields: [accounts.userId],
    references: [users.id],
  }),
}));

export const sessionsRelations = relations(sessions, ({ one }) => ({
  user: one(users, {
    fields: [sessions.userId],
    references: [users.id],
  }),
}));

export const subscriptionsRelations = relations(
  subscriptions,
  ({ one, many }) => ({
    user: one(users, {
      fields: [subscriptions.userId],
      references: [users.id],
    }),
    /** All payment events linked to this subscription */
    payments: many(payments),
  })
);

export const paymentsRelations = relations(payments, ({ one }) => ({
  user: one(users, {
    fields: [payments.userId],
    references: [users.id],
  }),
  subscription: one(subscriptions, {
    fields: [payments.subscriptionId],
    references: [subscriptions.id],
  }),
}));

export const assetsRelations = relations(assets, ({ many }) => ({
  /** All posts (both AI insights and ICT bias reports) for this asset */
  posts: many(posts),
}));

export const postsRelations = relations(posts, ({ one, many }) => ({
  /** The admin user who authored or triggered this post */
  author: one(users, {
    fields: [posts.userId],
    references: [users.id],
  }),
  /** The financial instrument this post analyses */
  asset: one(assets, {
    fields: [posts.assetId],
    references: [assets.id],
  }),
  /** All users who have bookmarked this post */
  savedBy: many(savedPosts),
}));

export const savedPostsRelations = relations(savedPosts, ({ one }) => ({
  user: one(users, {
    fields: [savedPosts.userId],
    references: [users.id],
  }),
  post: one(posts, {
    fields: [savedPosts.postId],
    references: [posts.id],
  }),
}));

