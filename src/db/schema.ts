/**
 * posts — Unified content table for all insight types.
 *
 * ┌─────────────────────────────────────────────────────────────────────────────┐
 * │ F1 — AI Insight (type = 'ai_insight')                                       │
 * │   Author: engine.actions.ts (userId = admin system account)                 │
 * │   direction, biasType, confidence: computed by AI multi-model chain         │
 * │   isProOnly: false — free users see with 24h delay                          │
 * │   Published automatically by the Vercel cron job at 06:00 UTC               │
 * ├─────────────────────────────────────────────────────────────────────────────┤
 * │ F2 — ICT Daily Bias Report (type = 'ict_bias')                              │
 * │   Author: admin user via TipTap editor in /admin/posts                      │
 * │   direction, biasType, confidence: set manually by admin before publish     │
 * │   isProOnly: true — Premium gets instant access; Free unlocks after 24h     │
 * │   Published manually by admin; publishedAt is set on explicit publish action│
 * └─────────────────────────────────────────────────────────────────────────────┘
 *
 * The 24-hour ICT unlock logic in the feed reads:
 *   isPro = true OR now() > publishedAt + INTERVAL '24 hours'
 *
 * FK types:
 *   userId  = text    (matches users.id — text)    Bug #11 prevention
 *   assetId = integer (matches assets.id — serial)  Bug #11 prevention
 */
export const posts = pgTable(
  "posts",
  {
    id: serial("id").primaryKey(),

    // TEXT — must match users.id (text). Bug #11 prevention.
    userId: text("user_id")
      .notNull()
      .references(() => users.id, { onDelete: "restrict" }),

    // INTEGER — must match assets.id (serial). Bug #11 prevention.
    assetId: integer("asset_id")
      .notNull()
      .references(() => assets.id, { onDelete: "restrict" }),

    type: postTypeEnum("type").notNull().default("ai_insight"),
    status: postStatusEnum("status").notNull().default("draft"),

    // ── Standard content fields ──
    /** Display title (e.g. "EURUSD — Bearish Continuation Setup | April 15") */
    title: text("title").notNull(),

    /**
     * HTML body from TipTap editor (ICT posts) or AI-generated HTML (AI insights).
     * Rendered directly in the feed card and insight detail view.
     */
    body: text("body").notNull(),

    // ── AI-generated / admin-set meta fields ──
    /**
     * Primary directional call — the most prominent data point on the feed card.
     * AI-computed for ai_insight; admin-selected for ict_bias.
     */
    direction: directionEnum("direction"),

    /**
     * Granular tone / bias type.
     * For most posts mirrors `direction`. Supports hawkish/dovish for F6 posts.
     */
    biasType: biasTypeEnum("bias_type"),

    /**
     * AI confidence score: 0–100.
     * Displayed as a progress bar on the feed card.
     * For ICT posts: admin-entered qualitative confidence (50 / 75 / 100).
     */
    confidence: integer("confidence"),

    /**
     * Gates the 24-hour unlock behaviour for ICT posts.
     * true  → Premium-only until publishedAt + 24h, then unlocked for Free
     * false → Available to Free immediately (AI insights)
     */
    isProOnly: boolean("is_pro_only").notNull().default(false),

    /**
     * Populated when admin clicks "Publish" or when the engine cron publishes.
     * NULL while status = 'draft'. Used for 24h ICT unlock arithmetic.
     */
    publishedAt: timestamp("published_at", { mode: "date" }),

    // ── Audit ──
    createdAt: timestamp("created_at", { mode: "date" }).notNull().defaultNow(),
    updatedAt: timestamp("updated_at", { mode: "date" }).notNull().defaultNow(),
  },
  (post) => ({
    userIdIdx: index("posts_user_id_idx").on(post.userId),
    assetIdIdx: index("posts_asset_id_idx").on(post.assetId),
    statusIdx: index("posts_status_idx").on(post.status),
    typeIdx: index("posts_type_idx").on(post.type),
    /** Indexed for feed ordering and 24h unlock arithmetic */
    publishedAtIdx: index("posts_published_at_idx").on(post.publishedAt),
    /** Composite index for the primary feed query: status + type + publishedAt */
    feedQueryIdx: index("posts_feed_query_idx").on(
      post.status,
      post.type,
      post.publishedAt
    ),
  })
);

/**
 * savedPosts — User bookmarking / reading list feature.
 *
 * Used in the /saved route and powered by save-insight.ts server action
 * with optimistic UI updates (React 19 useOptimistic).
 *
 * Composite unique key on (userId, postId) prevents duplicate bookmarks and
 * enables safe upsert patterns in the save/unsave toggle action.
 *
 * FK types:
 *   userId = text    (matches users.id — text)    Bug #11 prevention
 *   postId = integer (matches posts.id — serial)   Bug #11 prevention
 */
export const savedPosts = pgTable(
  "saved_posts",
  {
    id: serial("id").primaryKey(),

    // TEXT — must match users.id (text). Bug #11 prevention.
    userId: text("user_id")
      .notNull()
      .references(() => users.id, { onDelete: "cascade" }),

    // INTEGER — must match posts.id (serial). Bug #11 prevention.
    postId: integer("post_id")
      .notNull()
      .references(() => posts.id, { onDelete: "cascade" }),

    savedAt: timestamp("saved_at", { mode: "date" }).notNull().defaultNow(),
  },
  (saved) => ({
    /**
     * Composite unique index prevents duplicate bookmarks and serves as the
     * lookup key for the toggle action: "is this post saved by this user?"
     */
    uniqueUserPostIdx: uniqueIndex("saved_posts_user_post_idx").on(
      saved.userId,
      saved.postId
    ),
    userIdIdx: index("saved_posts_user_id_idx").on(saved.userId),
    postIdIdx: index("saved_posts_post_id_idx").on(saved.postId),
  })
);

